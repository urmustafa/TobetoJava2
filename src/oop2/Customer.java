package oop2;

public class Customer {
	private int id;
	private String cutomerNumer;
	private String phone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCutomerNumer() {
		return cutomerNumer;
	}

	public void setCutomerNumer(String cutomerNumer) {
		this.cutomerNumer = cutomerNumer;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
